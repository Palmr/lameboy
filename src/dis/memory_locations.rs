pub fn get_memory_comment(address: u16) -> Option<&'static str> {
    match address {
        // Resets
        0x0000 => Some("RST_00"),
        0x0008 => Some("RST_08"),
        0x0010 => Some("RST_10"),
        0x0018 => Some("RST_18"),
        0x0020 => Some("RST_20"),
        0x0028 => Some("RST_28"),
        0x0030 => Some("RST_30"),
        0x0038 => Some("RST_38"),
        // Interrupts
        0x0040 => Some("VBlankInterrupt"),
        0x0048 => Some("LCDCInterrupt"),
        0x0050 => Some("TimerOverflowInterrupt"),
        0x0058 => Some("SerialTransferCompleteInterrupt"),
        0x0060 => Some("JoypadTransitionInterrupt"),
        // Misc
        0x0100 => Some("Boot"),
        0x0104 => Some("HeaderLogo"),
        0x0134 => Some("HeaderTitle"),
        0x013f => Some("HeaderManufacturerCode"),
        0x0143 => Some("HeaderCGBFlag"),
        0x0144 => Some("HeaderNewLicenseeCode"),
        0x0146 => Some("HeaderSGBFlag"),
        0x0147 => Some("HeaderCartridgeType"),
        0x0148 => Some("HeaderROMSize"),
        0x0149 => Some("HeaderRAMSize"),
        0x014a => Some("HeaderDestinationCode"),
        0x014b => Some("HeaderOldLicenseeCode"),
        0x014c => Some("HeaderMaskROMVersion"),
        0x014d => Some("HeaderComplementCheck"),
        0x014e => Some("HeaderGlobalChecksum"),
        // Joypad
        0xFF00 => Some("rP1"),
        // Comm Port
        0xFF01 => Some("rSB"),
        0xFF02 => Some("rSC"),
        // Timer
        0xFF04 => Some("rDIV"),
        0xFF05 => Some("rTIMA"),
        0xFF06 => Some("rTMA"),
        0xFF07 => Some("rTAC"),
        // Interrupt Flag
        0xFF0F => Some("rIF"),
        // PPU
        0xFF40 => Some("rLCDC"),
        0xFF41 => Some("rSTAT"),
        0xFF42 => Some("rSCY"),
        0xFF43 => Some("rSCX"),
        0xFF44 => Some("rLY"),
        0xFF45 => Some("rLYC"),
        0xFF46 => Some("rDMA"),
        0xFF47 => Some("rBGP"),
        0xFF48 => Some("rOBP0"),
        0xFF49 => Some("rOBP1"),
        0xFF4A => Some("rWY"),
        0xFF4B => Some("rWX"),
        0xFF4D => Some("rKEY1"),
        0xFF4F => Some("rVBK"),
        0xFF51 => Some("rHDMA1"),
        0xFF52 => Some("rHDMA2"),
        0xFF53 => Some("rHDMA3"),
        0xFF54 => Some("rHDMA4"),
        0xFF55 => Some("rHDMA5"),
        // Infrared
        0xFF56 => Some("rRP"),
        // Palette
        0xFF68 => Some("rBCPS"),
        0xFF69 => Some("rBCPD"),
        0xFF6A => Some("rOCPS"),
        0xFF6B => Some("rOCPD"),
        // Ram Bank
        0xFF70 => Some("rSVBK"),
        // Interrupt
        0xFFFF => Some("rIE"),
        // Sound Control
        0xFF24 => Some("rNR50"),
        0xFF25 => Some("rNR51"),
        0xFF26 => Some("rNR52"),
        0xFF10 => Some("rNR10"),
        0xFF11 => Some("rNR11"),
        0xFF12 => Some("rNR12"),
        0xFF13 => Some("rNR13"),
        0xFF14 => Some("rNR14"),
        0xFF16 => Some("rNR21"),
        0xFF17 => Some("rNR22"),
        0xFF18 => Some("rNR23"),
        0xFF19 => Some("rNR24"),
        0xFF1A => Some("rNR30"),
        0xFF1B => Some("rNR31"),
        0xFF1C => Some("rNR32"),
        0xFF1D => Some("rNR33"),
        0xFF1E => Some("rNR34"),
        0xFF20 => Some("rNR41"),
        0xFF21 => Some("rNR42"),
        0xFF22 => Some("rNR43"),
        0xFF23 => Some("rNR44"),
        0xFF76 => Some("rPCM12"),
        0xFF77 => Some("rPCM34"),

        _ => None,
    }
}
